if(~isempty(bdroot))
    try
        rmdir slprj\ s
        disp('***************************************************************************************************')
        disp('Delete slprj fold!')
    catch
    end
    folder_name = [bdroot '*'];
    try
        rmdir(folder_name,'s')
        disp('***************************************************************************************************')
        disp('Delete folder of code generation of the model!')
    catch
    end
    try 
        delete(['*.slxc'])
        disp('***************************************************************************************************')
        disp('Delete the temp file of the model!')
    catch
    end
    clear folder_name;
    try
        disp('***************************************************************************************************')
        disp(['Model Name:   ' bdroot])
        disp('***************************************************************************************************')
        disp('Start code genration')
        slbuild(bdroot)
        disp('***************************************************************************************************')
        disp('Completed!')
        disp('***************************************************************************************************')
    catch
        disp('***************************************************************************************************')
        disp('Failure!')
        disp('***************************************************************************************************')
    end
else
    disp('***************************************************************************************************')
    disp('No opened model')
    disp('***************************************************************************************************')
end
